public class Contacts {
    String phoneNo;
    String name;

    public Contacts(String phoneNo, String name) {
        this.phoneNo = phoneNo;
        this.name = name;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
